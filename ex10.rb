# set tabby_cat variable to a string that is tabbed in when printed
tabby_cat = "\tI'm tabbed in."
# set persian_cat variable to a string split on two different lines
persian_cat = "I'm split\non a line."
# set backslash_cat variable to string that adds two \ around a
backslash_cat = "I'm \\ a \\ cat."

# sets fat_cat variable to a multi line string that is tabbed and has one 
# new line that is also tabbed
fat_cat = """
I'll do a list:
\t* Cat food
\t* Fishies
\t* Catnip\n\t* Grass
"""

# puts all the different variables
puts tabby_cat
puts persian_cat
puts backslash_cat
puts fat_cat